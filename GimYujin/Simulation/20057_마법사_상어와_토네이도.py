N = int(input())
graph = list(list(map(int, input().split())) for _ in range(N))

tornado = [[(-1, 0), (1, 0), (-2, -1), (2, -1), (0, -3), (-1, -1), (1, -1), (-1, -2), (1, -2)],
           [(0, -1), (0, 1), (1, -2), (1, 2), (3, 0), (1, -1), (1, 1), (2, -1), (2, 1)],
           [(-1, 0), (1, 0), (-2, 1), (2, 1), (0, 3), (-1, 1), (1, 1), (-1, 2), (1, 2)],
           [(0, -1), (0, 1), (1, -2), (1, 2), (-3, 0), (1, -1), (1, 1), (-2, -1), (-2, 1)]]

def move():
    x, y = N // 2, N // 2
    cnt = 0
    direction = [(0, -1), (1, 1), (0, 1), (-1, 0)]
    print(x, y)

    for i in range(1, N):
        for _ in range(2):
            cnt %= 4
            nx = x + i * direction[cnt][0]
            ny = y + i * direction[cnt][1]

            if 0 <= nx < N and 0 <= ny < N:
                print(nx, ny)
                print("cnt == ", cnt)
                if cnt == 0:
                    for m in range(y+1, ny-1, -1):
                        print(nx, m, graph[nx][m])
                    print()
                elif cnt == 1:
                    for n in range(x+1, nx+1):
                        # graph[n][ny]
                        print(n, ny, graph[n][ny])
                elif cnt == 2:
                    for m in range(y, ny+1):
                        print(nx, m, graph[nx][m])
                else:
                    for n in range(nx, x-1, -1):
                        print(n, ny, graph[n][ny])

                x = nx
                y = ny

            cnt += 1
            print(x, y, i)

move()

